numero_um = int(input("Escreva o primeiro número: "))
numero_dois = int(input("Escreva o segundo número: "))
if (numero_um > numero_dois):
    escreva = "O primeiro número é maior"
elif (numero_dois > numero_um):
    escreva = "O segundo número é maior"
else:
    escreva = "Os números são iguais"
print(escreva)
    
