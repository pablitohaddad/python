print("Imprimindo os números de 0 a 9:")
for x in range(10):
    print(x, end=" ")
print("\nImprimindo os números de 1 a 20:")
for x in range(1, 21):
    print(x, end=" ")
print("\nImprimindo os números pares de 0 a 20:")
for x in range(0, 21, 2):
    print(x, end=" ")
print("\nImprimindo os números impares de 1 a 20:")
for x in range(1, 21, 2):
    print(x, end=" ")
print("\nImprimindo os números impares de 20 a 1")
for x in range(20, 0, -1):
    print(x, end=" ")